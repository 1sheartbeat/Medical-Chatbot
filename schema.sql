-- View 1: Session activity summary
CREATE OR REPLACE VIEW session_activity_summary AS
SELECT 
    s.session_id,
    s.user_ip,
    s.message_count,
    s.created_at AS session_start,
    s.last_active AS last_activity,
    TIMESTAMPDIFF(MINUTE, s.created_at, s.last_active) AS session_duration_minutes,
    COUNT(c.id) AS actual_messages
FROM sessions s
LEFT JOIN conversations c ON s.session_id = c.session_id
GROUP BY s.id
ORDER BY s.last_active DESC;

-- View 2: Daily conversation statistics
CREATE OR REPLACE VIEW daily_stats AS
SELECT 
    DATE(created_at) AS date,
    COUNT(*) AS total_conversations,
    COUNT(DISTINCT session_id) AS unique_sessions,
    AVG(response_time_ms) AS avg_response_time_ms,
    SUM(CASE WHEN intent_detected = 'symptom' THEN 1 ELSE 0 END) AS symptom_queries,
    SUM(CASE WHEN intent_detected = 'medication' THEN 1 ELSE 0 END) AS medication_queries,
    SUM(CASE WHEN intent_detected = 'emergency' THEN 1 ELSE 0 END) AS emergency_queries
FROM conversations
GROUP BY DATE(created_at)
ORDER BY date DESC;

