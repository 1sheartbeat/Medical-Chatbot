-- ------------------------------------------------------------
-- Table 1: conversations
-- Stores all conversation history between users and the chatbot
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS conversations (
    -- Primary key: unique identifier for each conversation turn
    id INT AUTO_INCREMENT PRIMARY KEY COMMENT 'Unique record identifier',
    
    -- Session identifier: groups messages from the same user session
    session_id VARCHAR(64) NOT NULL COMMENT 'Unique session identifier for grouping conversations',
    
    -- User input: the message sent by the user
    user_message TEXT NOT NULL COMMENT 'User input message content',
    
    -- Bot response: the reply generated by the Llama 3 8B model
    bot_response TEXT NOT NULL COMMENT 'Chatbot response generated by Llama 3 8B',
    
    -- Intent detection: identified user intent (symptom, medication, emergency, etc.)
    intent_detected VARCHAR(32) DEFAULT 'general' COMMENT 'Detected user intent from NLP analysis',
    
    -- Performance metric: time taken to generate response in milliseconds
    response_time_ms INT DEFAULT 0 COMMENT 'Response generation time in milliseconds',
    
    -- Timestamp: when the conversation occurred
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT 'Record creation timestamp',
    
    -- Indexes for faster query performance
    INDEX idx_session_id (session_id) COMMENT 'Index for session-based queries',
    INDEX idx_created_at (created_at) COMMENT 'Index for time-based queries',
    INDEX idx_intent (intent_detected) COMMENT 'Index for intent-based filtering'
    
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci
COMMENT='Stores all conversation history between users and chatbot';

-- ------------------------------------------------------------
-- Table 2: sessions
-- Tracks user sessions and metadata
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS sessions (
    -- Primary key
    id INT AUTO_INCREMENT PRIMARY KEY COMMENT 'Unique session record identifier',
    
    -- Session identifier: unique per user session
    session_id VARCHAR(64) NOT NULL UNIQUE COMMENT 'Unique session identifier for tracking',
    
    -- User IP address for analytics and security
    user_ip VARCHAR(45) COMMENT 'User IP address (supports IPv4 and IPv6)',
    
    -- User agent string for browser/device analytics
    user_agent VARCHAR(512) COMMENT 'User browser or device information',
    
    -- Session statistics
    message_count INT DEFAULT 0 COMMENT 'Total number of messages in this session',
    
    -- Timestamps
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT 'Session creation timestamp',
    last_active DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Last activity timestamp',
    
    -- Indexes
    INDEX idx_session_id (session_id) COMMENT 'Index for session lookup',
    INDEX idx_last_active (last_active) COMMENT 'Index for session cleanup'
    
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci
COMMENT='Tracks user sessions and metadata for analytics';

-- ------------------------------------------------------------
-- Table 3: feedback
-- Stores user feedback for quality improvement (optional)
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS feedback (
    -- Primary key
    id INT AUTO_INCREMENT PRIMARY KEY COMMENT 'Unique feedback identifier',
    
    -- Foreign key linking to conversation
    conversation_id INT COMMENT 'Reference to the conversation being rated',
    
    -- Session identifier
    session_id VARCHAR(64) COMMENT 'Session identifier for grouping',
    
    -- User rating
    rating TINYINT COMMENT 'User rating from 1 to 5 stars',
    
    -- Detailed feedback text
    feedback_text TEXT COMMENT 'User feedback comments',
    
    -- Binary flag for helpfulness
    is_helpful BOOLEAN DEFAULT NULL COMMENT 'Whether the user found the response helpful',
    
    -- Timestamp
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT 'Feedback submission timestamp',
    
    -- Indexes
    INDEX idx_conversation_id (conversation_id) COMMENT 'Index for conversation-based queries',
    INDEX idx_session_id (session_id) COMMENT 'Index for session-based queries',
    
    -- Foreign key constraint: when a conversation is deleted, keep the feedback but nullify the reference
    FOREIGN KEY (conversation_id) REFERENCES conversations(id) ON DELETE SET NULL
    
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci
COMMENT='Stores user feedback for chatbot quality improvement';

